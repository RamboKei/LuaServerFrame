function table.values(hashtable)
    local values = {}
    for k, v in pairs(hashtable) do
        values[#values + 1] = v
    end
    return values
end

-- 源串和目标串key差异
function table.kdiff(src, dest)
    local diff = {};
    local bfind = false;
    for i, v in pairs(src) do
      bfind = false;
      for j, k in pairs(dest) do
        if i == j then
          bfind = true;
          break;
        end
      end

      if not bfind then
        diff[i] = v;
      end
    end

    return diff;
end

-- 源串和目标串value差异
function table.vdiff(src, dest)
    local diff = {};
    local bfind = false;
    for i, v in pairs(src) do
      bfind = false;
      for j, k in pairs(dest) do
        if v == k then
          bfind = true;
          break;
        end
      end

      if not bfind then
        diff[i] = v;
      end
    end

    return diff;
end

function table.vposdiff(src, dest)
    local diff = {};
    local bfind = false;
    for i, v in pairs(src) do
      bfind = false;
      for j, k in pairs(dest) do
        if v.x == k.x and v.y == k.y then
          bfind = true;
          break;
        end
      end

      if not bfind then
        diff[i] = v;
      end
    end

    return diff;
end